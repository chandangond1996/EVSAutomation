package RinkuSir.Switch_Case;

public class Switch_Practice {

	public static void main(String[] args) {

		int key = 2;
		switch (key) {
		case 1: {
			System.out.println("one");
			break;
		}
		case 2: {
			System.out.println("Two");
			break;
		}
		case 3: {
			System.out.println("Three");
			break;
		}
		default:
			System.out.println("No List No.......");
			break;
		}
	}

}
